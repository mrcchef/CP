ll cal(ll sm,ll mx)
{
    ll diff=n-sm;
    if(diff<0)
        return 0;
    else if(diff==0 && mx>=d)
    {
        return dp[sm][mx]=1;
    }
    else if(diff==0)
        return dp[sm][mx]=0;
    
    if(dp[sm][mx]!=-1)
        return dp[sm][mx];

    ll cnt=0;

    for(ll i=1;i<=min(diff,k);i++)
    {
        cnt+=cal(sm+i,max(i,mx));
        cnt=cnt%mod;
    }

    return dp[sm][mx]=cnt;
}

